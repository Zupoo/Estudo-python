def soma (n1, n2):
    '''faz a soma de dois numeros e devolve como resposta um numero

    Args:
        n1 | primeiro numero a ser somado
        n2 | segundo numero a ser somado

    Returns:
        int | soma dos valores                
    '''
    soma = n1 + n2
    return soma

